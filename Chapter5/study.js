let members = ['쿤갈레', 'Gerrard66', '우리생각해써','흙토끼','End Miracle']

console.log(members);

// 배열의 첫 요소를 삭제: shift()
members.shift();
console.log(members);

// 배열의 마지막 요소를 삭제 : pop()

members.pop();
console.log(members);

// 베열의 첫 요소로 값 추가: unshift(value)
members.unshift('NiceCode')
console.log(members)

// 배열의 마지막 요소로 값 추가: push(value)
members.push('GoodCode')
console.log(members)